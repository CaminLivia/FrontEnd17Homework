// All numbers from 1 to 20
function numbersFrom1To20() {
  for (var i = 1; i <= 20; i++) {
    console.log(i);
  }
}

console.log("All numbers from 1 to 20");
numbersFrom1To20();

// Numbers from an interval
function numbersFromInterval(start, finish) {
  for (var j = start; j <= finish; j++) {
    console.log(j);
  }
}

console.log("Numbers from an interval: 1 to 20");
numbersFromInterval(1, 20);
